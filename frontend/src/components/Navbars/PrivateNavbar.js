import { Fragment } from 'react';
import { Disclosure, Menu, Transition } from '@headlessui/react';
import { Bars3Icon, XMarkIcon } from '@heroicons/react/24/outline';
import { PlusIcon } from '@heroicons/react/20/solid';
import { FiLogOut } from 'react-icons/fi';
import { useMutation } from '@tanstack/react-query';
import { logoutAPI } from '../../apis/user/usersAPI';
import { useAuth } from '../../AuthContext/AuthContext';
import logo from '../../assets/logo.png';
import { getUserProfileAPI } from '../../apis/user/usersAPI';
import { useQuery } from '@tanstack/react-query';
import { Link } from 'react-router-dom';
import { useNavigate } from 'react-router-dom';

const navigation = [
	{ name: 'Dashboard', href: '/dashboard', current: true },
	{ name: 'Pricing', href: '/plans', current: true },
	{ name: 'Generation History', href: '/history', current: true },
];
const userNavigation = [{ name: 'Sign out', href: '/' }];

function classNames(...classes) {
	return classes.filter(Boolean).join(' ');
}

export default function PrivateNavbar() {
	const redirect = useNavigate();

	//check user authentication
	const { logout } = useAuth();
	const { data } = useQuery({ queryFn: getUserProfileAPI, queryKey: ['userProfile'] });

	//mutation
	const mutation = useMutation({ mutationFn: logoutAPI });
	//handle logout
	const handleLogout = async () => {
		try {
			await mutation.mutateAsync();
			logout();
			redirect('/');
		} catch (error) {
			console.error('Failed to logout:', error);
		}
	};

	return (
		<Disclosure as="nav" className="bg-gray-900 border-b  border-black shadow-md">
			{({ open }) => (
				<>
					<div className="mx-auto max-w-7xl px-4 sm:px-6 lg:px-8">
						<div className="flex h-16 justify-between">
							<div className="flex">
								<div className="-ml-2 mr-2 flex items-center md:hidden">
									{/* Mobile menu button */}
									<Disclosure.Button className="relative inline-flex items-center justify-center rounded-md p-2 text-gray-400 hover:bg-gray-700 hover:text-white focus:outline-none focus:ring-2 focus:ring-inset focus:ring-white">
										<span className="absolute -inset-0.5" />
										<span className="sr-only">Open main menu</span>
										{open ? (
											<XMarkIcon className="block h-6 w-6" aria-hidden="true" />
										) : (
											<Bars3Icon className="block h-6 w-6" aria-hidden="true" />
										)}
									</Disclosure.Button>
								</div>
								<div className="flex flex-shrink-0 m-2 items-center">
									{/* logo */}
									<Link to="/">
										<img src={logo} alt="Logo" p-4 style={{ height: '60px', width: '90px' }} />
									</Link>
								</div>
								<div className="hidden md:ml-6 md:flex md:items-center md:space-x-4">
									{navigation.map((item) => (
										<Link
											key={item.name}
											to={item.href}
											className={classNames(
												item.current ? 'bg-gray-900 text-white' : 'text-gray-300 hover:bg-gray-700 hover:text-white',
												'rounded-md px-3 py-2 text-sm font-medium'
											)}
											aria-current={item.current ? 'page' : undefined}>
											{item.name}
										</Link>
									))}
								</div>
							</div>
							<div className="flex items-center">
								<div className="flex-shrink-0">
									<Link
										to="/generate-content"
										className="relative animate-bounce inline-flex items-center gap-x-1.5 rounded-md bg-purple-500 px-3 py-2 text-sm font-semibold text-white shadow-sm hover:bg-indigo-400 focus-visible:outline focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-500">
										<PlusIcon className="-ml-0.5 h-5 w-5" aria-hidden="true" />
										Generate content
									</Link>

									<button
										onClick={handleLogout}
										type="button"
										className="ml-2 relative inline-flex items-center gap-x-1.5 rounded-md bg-red-500 px-3 py-2 text-sm font-semibold text-white shadow-sm hover:bg-indigo-400 focus-visible:outline focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-500">
										<FiLogOut className="h-5 w-5" aria-hidden="true" />
									</button>
								</div>
								<div className="hidden md:ml-4 md:flex md:flex-shrink-0 md:items-center">
									<Menu as="div" className="relative ml-3">
										<Transition
											as={Fragment}
											enter="transition ease-out duration-200"
											enterFrom="transform opacity-0 scale-95"
											enterTo="transform opacity-100 scale-100"
											leave="transition ease-in duration-75"
											leaveFrom="transform opacity-100 scale-100"
											leaveTo="transform opacity-0 scale-95">
											<Menu.Items className="absolute right-0 z-10 mt-2 w-48 origin-top-right rounded-md bg-white py-1 shadow-lg ring-1 ring-black ring-opacity-5 focus:outline-none">
												{userNavigation.map((item) => (
													<Menu.Item key={item.name}>
														{({ active }) => (
															<Link
																key={item.name}
																to={item.href}
																className={classNames(
																	active ? 'bg-gray-100' : '',
																	'block px-4 py-2 text-sm text-gray-700'
																)}>
																{item.name}
															</Link>
														)}
													</Menu.Item>
												))}
											</Menu.Items>
										</Transition>
									</Menu>
								</div>
							</div>
						</div>
					</div>

					<Disclosure.Panel className="md:hidden">
						<div className="space-y-1 px-2 pb-3 pt-2 sm:px-3">
							{navigation.map((item) => (
								<Link
									key={item.name}
									to={item.href}
									className={classNames(
										item.current ? 'bg-gray-900 text-white' : 'text-gray-300 hover:bg-gray-700 hover:text-white',
										'block rounded-md px-3 py-2 text-sm font-medium'
									)}
									aria-current={item.current ? 'page' : undefined}>
									{item.name}
								</Link>
							))}
						</div>
						<div className="border-t border-gray-700 pb-3 pt-4">
							<div className="flex items-center px-5 sm:px-6">
								<div className="ml-3">
									<div className="text-base font-medium text-white">{data?.user.username}</div>
									<div className="text-sm font-medium text-gray-400">{data?.user.email}</div>
								</div>
							</div>
							<div className="mt-3 space-y-1 px-2 sm:px-3">
								{userNavigation.map((item) => (
									<Link
										onClick={handleLogout}
										key={item.name}
										to={item.href}
										className="block rounded-md px-3 py-2 text-base font-medium text-gray-400 hover:bg-gray-700 hover:text-white">
										{item.name}
									</Link>
								))}
							</div>
						</div>
					</Disclosure.Panel>
				</>
			)}
		</Disclosure>
	);
}
